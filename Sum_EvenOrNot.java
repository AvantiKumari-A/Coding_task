class Sum_EvenOrNot{
public static void main(String[] args){
int a=10,b=20,sum;
sum=a+b;
System.out.println("The sum of " + a + " + " + b + " is: " + sum);
if(sum%2==0){
System.out.println("Sum of " + a + " + " + b + " = " + sum + " is even ");
}
else{
System.out.println("Sum of " + a + " + " + b + " = " + sum + " is odd ");
}
}
}  